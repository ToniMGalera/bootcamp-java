
public class Tasca42 {

	public static void main(String[] args) {
		//declarar variables
		int N = 5;
        double A = 10.5;
        char C = 'X';
        
        // Mostrar valor de cada variable
        System.out.println("Valor de N: " + N);
        System.out.println("Valor de A: " + A);
        System.out.println("Valor de C: " + C);

        // Mostrar suma de N + A
        System.out.println("Suma de N + A: " + (N + A));

        // Mostrar diferencia de A - N
        System.out.println("Diferencia de A - N: " + (A - N));

        // Mostrar valor numérico correspondiente al carácter que contiene la variable C
        System.out.println("Valor numérico de C: " + (int) C);
      

	}

}
