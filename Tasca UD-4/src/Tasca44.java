
public class Tasca44 {

	public static void main(String[] args) {
		 // Declarar variable entera N y asignarle un valor
        int N = 50;

        // Incrementar N en 77
        N += 77;
        System.out.println("N incrementada en 77: " + N);

        // Decrementar N en 3
        N -= 3;
        System.out.println("N decrementada en 3: " + N);

        // Duplicar su valor
        N *= 2; //N=N*2;//150
        System.out.println("N duplicada: " + N);

	}

}
