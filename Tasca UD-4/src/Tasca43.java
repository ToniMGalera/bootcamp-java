
public class Tasca43 {

	public static void main(String[] args) {
		// Declarar variables de tipo int
        int X = 10;
        int Y = 5;

        // Declarar variables de tipo double
        double N = 7.5;
        double M = 2.5;

        // Mostrar por pantalla el valor de cada variable
        System.out.println("Valor de X: " + X);
        System.out.println("Valor de Y: " + Y);
        System.out.println("Valor de N: " + N);
        System.out.println("Valor de M: " + M);

        // Operaciones con variables de tipo int
        System.out.println("Suma X + Y: " + (X + Y));
        System.out.println("Diferencia X - Y: " + (X - Y));
        System.out.println("Producto X * Y: " + (X * Y));
        System.out.println("Cociente X / Y: " + (X / Y));
        System.out.println("Resto X % Y: " + (X % Y));

        // Operaciones con variables de tipo double
        System.out.println("Suma N + M: " + (N + M));
        System.out.println("Diferencia N - M: " + (N - M));
        System.out.println("Producto N * M: " + (N * M));
        System.out.println("Cociente N / M: " + (N / M));
        System.out.println("Resto N % M: " + (N % M));

        // Combinaci√≥n de variables
        System.out.println("Suma X + N: " + (X + N));
        System.out.println("Cociente Y / M: " + (Y / M));
        System.out.println("Resto Y % M: " + (Y % M));

        // Operaciones adicionales
        System.out.println("Doble de X: " + (2 * X));
        System.out.println("Doble de Y: " + (2 * Y));
        System.out.println("Doble de N: " + (2 * N));
        System.out.println("Doble de M: " + (2 * M));

        // Suma de todas las variables
        double sumaTotal = X + Y + N + M;
        System.out.println("Suma total de todas las variables: " + sumaTotal);

        // Producto de todas las variables
        double productoTotal = X * Y * N * M;
        System.out.println("Producto total de todas las variables: " + productoTotal);
	
	}
	}
